package tech.rtsproduction.chitchat;

public class ChatProfile {

    private String profileName;
    private String profileLastSeen;
    private String profileDisplayImage;

    public String getProfileName() {
        return profileName;
    }

    public void setProfileName(String profileName) {
        this.profileName = profileName;
    }

    public String getProfileLastSeen() {
        return profileLastSeen;
    }

    public void setProfileLastSeen(String profileLastSeen) {
        this.profileLastSeen = profileLastSeen;
    }

    public String getProfileDisplayImage() {
        return profileDisplayImage;
    }

    public void setProfileDisplayImage(String profileDisplayImage) {
        this.profileDisplayImage = profileDisplayImage;
    }
}
